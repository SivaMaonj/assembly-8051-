ORG 0H
MOV DPTR,#8500H
MOVX A,@DPTR
CJNE A,#01H,L1
SJMP ADD
L1:
CJNE A,#02H,L2
SJMP MUL

L2:
CJNE A,#03H,L3	
SJMP SUB

L3:
SJMP DIV


ADD:
INC DPTR
MOVX A,@DPTR
MOV B,A
INC DPTR
MOVX A,@DPTR
ADD A,B
INC DPTR
MOVX @DPTR,A
SJMP EXIT

MUL:
INC DPTR
MOVX A,@DPTR
MOV B,A
INC DPTR
MOVX A,@DPTR
MUL AB
INC DPTR
MOVX @DPTR,A
INC DPTR
MOV A,B
MOVX @DPTR,A
SJMP EXIT

SUB:
INC DPTR
MOVX A,@DPTR
MOV B,A
INC DPTR
MOVX A,@DPTR
SUBB A,B
INC DPTR
MOVX @DPTR,A
SJMP EXIT

DIV:
INC DPTR
MOVX A,@DPTR
MOV B,A
INC DPTR
MOVX A,@DPTR
DIV AB
INC DPTR
MOVX @DPTR,A
INC DPTR
MOV A,B
MOVX @DPTR,A
SJMP EXIT
EXIT:SJMP EXIT

END


